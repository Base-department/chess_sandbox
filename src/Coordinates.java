public class Coordinates {
    int first, second;
    Coordinates(int X, int Y){
        first = X;
        second = Y;
    }
    public int getFirst(){
        return first;
    }
    public int getSecond(){
        return second;
    }
    public void setFirst(int X){
        this.first = X;
    }
    public void setSecond(int Y){
        this.second = Y;
    }
}
